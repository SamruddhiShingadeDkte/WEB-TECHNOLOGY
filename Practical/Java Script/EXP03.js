//for loop in array :
let arr = [1,2,3,4];
let i;
for(i=0;i<arr.length;i++){
    console.log(`arr[${i}] : ${arr[i]}`)
}

//while loop in array :
i=0;
while(i<arr.length){
    console.log(`arr[${i}] : ${arr[i]}`)
    i++;
}

//do while loop in array :
i=0;
do{
    console.log(`arr[${i}] : ${arr[i]}`)
    i++;
}while(i<arr.length);

//for each loop in array :
arr.forEach()